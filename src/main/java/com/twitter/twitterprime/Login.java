    private void jLabel2ComponentResized(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_jLabel2ComponentResized
        // Carga la imagen como ImageIcon
    ImageIcon originalIcon = new ImageIcon(getClass().getResource("/images/logo.png"));
    Image imagenOriginal = originalIcon.getImage();

    // Dimensiones originales
    int imgWidth = originalIcon.getIconWidth();
    int imgHeight = originalIcon.getIconHeight();
    double imgAspect = (double) imgWidth / (double) imgHeight;

    // Dimensiones del JLabel (despuÃ©s de redimensionarse)
    int labelWidth = jLabel2.getWidth();
    int labelHeight = jLabel2.getHeight();
    double labelAspect = (double) labelWidth / (double) labelHeight;

    // Calculamos ancho y alto sin deformar
    int newWidth;
    int newHeight;
    if (imgAspect > labelAspect) {
        // Ajuste por ancho
        newWidth = labelWidth;
        newHeight = (int) (labelWidth / imgAspect);
    } else {
        // Ajuste por alto
        newHeight = labelHeight;
        newWidth = (int) (labelHeight * imgAspect);
    }

    // Escalar la imagen
    Image imagenEscalada = imagenOriginal.getScaledInstance(
        newWidth,
        newHeight,
        Image.SCALE_SMOOTH
    );

    // Crear nuevo icono y asignarlo
    ImageIcon iconoEscalado = new ImageIcon(imagenEscalada);
    jLabel2.setIcon(iconoEscalado);
    }//GEN-LAST:event_jLabel2ComponentResized

